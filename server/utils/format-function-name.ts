/**
 * Форматирует имя функции, заменяя кириллицу на транслитерацию и удаляя специальные символы
 * @param name Исходное имя функции
 * @returns Отформатированное имя функции
 */
export function formatFunctionName(name: string): string {
  if (!name) return "";

  // Таблица транслитерации кириллицы
  const translitMap: Record<string, string> = {
    а: "a",
    б: "b",
    в: "v",
    г: "g",
    д: "d",
    е: "e",
    ё: "e",
    ж: "zh",
    з: "z",
    и: "i",
    й: "y",
    к: "k",
    л: "l",
    м: "m",
    н: "n",
    о: "o",
    п: "p",
    р: "r",
    с: "s",
    т: "t",
    у: "u",
    ф: "f",
    х: "kh",
    ц: "ts",
    ч: "ch",
    ш: "sh",
    щ: "sch",
    ъ: "",
    ы: "y",
    ь: "",
    э: "e",
    ю: "yu",
    я: "ya",
    // Заглавные буквы
    А: "A",
    Б: "B",
    В: "V",
    Г: "G",
    Д: "D",
    Е: "E",
    Ё: "E",
    Ж: "Zh",
    З: "Z",
    И: "I",
    Й: "Y",
    К: "K",
    Л: "L",
    М: "M",
    Н: "N",
    О: "O",
    П: "P",
    Р: "R",
    С: "S",
    Т: "T",
    У: "U",
    Ф: "F",
    Х: "Kh",
    Ц: "Ts",
    Ч: "Ch",
    Ш: "Sh",
    Щ: "Sch",
    Ъ: "",
    Ы: "Y",
    Ь: "",
    Э: "E",
    Ю: "Yu",
    Я: "Ya",
  };

  // Транслитерация кириллицы
  let transliterated = name
    .split("")
    .map((char) => translitMap[char] || char)
    .join("");

  // Удаление специальных символов и замена пробелов на нижнее подчеркивание
  transliterated = transliterated
    .replace(/[^\w\s]/g, "") // Удаляем все символы, кроме букв, цифр и пробелов
    .replace(/\s+/g, "_"); // Заменяем пробелы на нижнее подчеркивание

  return transliterated;
}
